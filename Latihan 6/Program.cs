using System;

Console.WriteLine();

// while loop
int loopingnya = 0;
while (loopingnya < 1)
{
    Console.WriteLine("Loop 1");
    loopingnya++;
}

Console.WriteLine();

// do/while loop
int loop = 0;
do {
    Console.WriteLine("Loop 7");
    loop++;
}
while (loop < 7);

Console.WriteLine();

// for loop
for (int tes = 0; tes < 9; tes++)
{
    Console.WriteLine("Loop 9");
}